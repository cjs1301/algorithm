#include <bits/stdc++.h>
using namespace std;
int main(){
    int N;
    cin >> N;
    int count = 0;
    
    while(N > 0){
        if(N % 5 == 0){
            N -= 5;
            count++;
        }
        else{
            N -= 3;
            count++;
        }
    }
    if(N < 0){
        cout << -1 << '\n';
    }
    else{
        cout << count << '\n';
    }
    return 0;
}