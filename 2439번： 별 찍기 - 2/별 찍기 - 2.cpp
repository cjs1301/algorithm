#include <bits/stdc++.h>
using namespace std;
int main(){
    int n;
    scanf("%d", &n);

    for (int i = 1; i <= n; i++){
        
        for(int s = 0; s< n-i; s++){
            printf(" ");
        }
        for(int s = 0; s< i; s++){
            printf("*");
        }
        printf("\n");
    }
    return 0;
}